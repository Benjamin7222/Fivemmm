AddEventHandler(("%s:notMoney"):format(KaizoMasques.Utils.PrefixEvent), function()
    ESX.TriggerServerCallback('esx_skin:getPlayerSkin', function(skin)
        KaizoMasques.Utils.InSide('skinchanger:loadSkin', skin)
    end)
end)

